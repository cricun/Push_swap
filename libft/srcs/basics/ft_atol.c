long	ft_atol(const char *str)
{
	long a;

	a = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (!(*str >= '0' && *str <= '9') &&
			!((*str == '-' || *str == '+') && *(str + 1) >= '0'
				&& *(str + 1) <= '9'))
		return (0);
	if (*str++ == '-')
		while (*str >= '0' && *str <= '9')
			a = a * 10 + -(long)(*str++ - 48);
	else if (*--str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		a = a * 10 + (long)(*str++ - 48);
	return (a);
}
